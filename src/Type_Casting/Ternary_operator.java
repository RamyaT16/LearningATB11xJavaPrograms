package Type_Casting;

public class Ternary_operator {
    public static void main(String[] args) {
        int age = 21;
        String rights = (age>18) ? "you can vote" : " you cannot vote";// ternary operator ?:
        System.out.println(rights);
    }
}
/** ternary operator is the operator which can be used as if else statement in one line/
 * syntax is ?:
 * ? is like if asking
 * : is like else in one line
 */
