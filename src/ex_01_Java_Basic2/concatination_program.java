package ex_01_Java_Basic2;

public class concatination_program {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        String c = "Ramya";
        String d = "T";
        System.out.println(a + b + c + d);
        System.out.println("Ramya"+"t"+ a+b);
        System.out.println("Ramya"+"t"+ (a+b));
        System.out.println("Ramya\tT"+ (a+b)+ "Hope for Good" + "Always");
    }
}
