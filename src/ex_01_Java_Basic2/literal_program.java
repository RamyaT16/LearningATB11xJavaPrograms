package ex_01_Java_Basic2;

public class literal_program {
    public static void main(String[] args) {
        boolean a = true; //boolean literal
        System.out.println(a);
        int b = 43; //intergal literal
        System.out.println(b);
        float pi = 3.141f;
        System.out.println(pi);
        char c1 = 'a';
       System.out.println(c1);
       // char new_line = '/n'; // for new line
        System.out.println("Hello/nRamya");
    }
}
