package ex_01_Java_Basic2;

public class Constants_details {
    public static void main(String[] args) {
        int a = 10;
        a = 20;
        System.out.println(a);
        // contstant includes final keyword.
        final byte b = 25;
         // byte b = 30; cannot change
        System.out.println(b);
    }
}

