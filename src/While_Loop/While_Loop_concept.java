package While_Loop;

public class While_Loop_concept {
    // It is the entry controlled loop it will not execute until the condition is statisfied
    // initialization is outside.
    //condition must match
    //then only updation will happen
    public static void main(String[] args) {
        int i = 1;
        while(i< 20){
            System.out.println("print the value of" +i);
            i++;
        }
    }
}
